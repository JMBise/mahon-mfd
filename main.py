                if self.mode == 'route':
                        lines = self.routes[self.route]
                        cursor = self.cursor
                        mfd.display(lines[(cursor + 0) % len(lines)], lines[(cursor + 1) % len(lines)], lines[(cursor + 2) % len(lines)])
